package lesson8;

public class Cat {

    private String name;
    private int age;
    private String color;
    private boolean home;

    public Cat(String name, int age, String color, boolean home) {
        this.name = name;
        this.age = age;
        this.color = color;
        this.home = home;
    }


    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public String getColor() {
        return color;
    }

    public boolean getHome() {
        return home;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public void setHome(boolean home) {
        this.home = home;
    }












}
