package lesson8;

public class Dog {

    private String name;
    private int age;
    private String bread;

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public String getBread() {
        return bread;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setBread(String bread) {
        this.bread = bread;
    }

















}
